
//tipos de função

//declarativas, precisa de um nome
function funcao() {
    console.log("Função declarativa");
}
funcao();

//expressão de função-- nome opcional


//com nomeação
var funcao = function () {
    console.log("Função de expressão");
}
funcao();


//Arrow function -- não são nomeadas

var funcao =()=>{
console.log('Arrow function');
}
funcao();

